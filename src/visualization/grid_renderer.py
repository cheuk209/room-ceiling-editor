"""
Grid HTML Renderer

Renders Grid objects as interactive HTML visualizations showing
component placements at each stage of the pipeline.
"""

from typing import List, Tuple
from src.pipeline.data_models import Grid, ComponentType
from datetime import datetime


class GridHTMLRenderer:
    """Renders grids as HTML with color-coded components."""

    # Color scheme for different component types
    COLORS = {
        ComponentType.LIGHT: "#FFD700",          # Gold
        ComponentType.AIR_SUPPLY: "#87CEEB",     # Sky Blue
        ComponentType.SMOKE_DETECTOR: "#FF6B6B", # Red
    }

    def render_pipeline_stages(
        self,
        stages: List[Tuple[str, Grid]],
        output_path: str
    ) -> str:
        """
        Render multiple pipeline stages side-by-side.

        Args:
            stages: List of (stage_name, grid) tuples
            output_path: Where to save the HTML file

        Returns:
            Path to the generated HTML file
        """
        html = self._generate_html(stages)

        with open(output_path, 'w') as f:
            f.write(html)

        return output_path

    def _generate_html(self, stages: List[Tuple[str, Grid]]) -> str:
        """Generate complete HTML document."""

        stages_html = "\n".join([
            self._render_stage(name, grid, idx)
            for idx, (name, grid) in enumerate(stages)
        ])

        return f"""<!DOCTYPE html>
<html lang="en">
<head>
    <meta charset="UTF-8">
    <meta name="viewport" content="width=device-width, initial-scale=1.0">
    <title>Ceiling Grid Pipeline Visualization</title>
    <style>
        {self._get_css()}
    </style>
</head>
<body>
    <div class="container">
        <header>
            <h1>üèóÔ∏è Ceiling Grid ML Pipeline Results</h1>
            <p class="timestamp">Generated: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}</p>
        </header>

        <div class="legend">
            <h3>Component Types</h3>
            <div class="legend-items">
                <div class="legend-item">
                    <div class="legend-color" style="background-color: {self.COLORS[ComponentType.LIGHT]}"></div>
                    <span>Light</span>
                </div>
                <div class="legend-item">
                    <div class="legend-color" style="background-color: {self.COLORS[ComponentType.AIR_SUPPLY]}"></div>
                    <span>Air Supply</span>
                </div>
                <div class="legend-item">
                    <div class="legend-color" style="background-color: {self.COLORS[ComponentType.SMOKE_DETECTOR]}"></div>
                    <span>Smoke Detector</span>
                </div>
            </div>
        </div>

        <div class="stages-container">
            {stages_html}
        </div>

        <footer>
            <p>Generated by Ceiling Grid ML Pipeline | Apache Airflow</p>
        </footer>
    </div>

    <script>
        {self._get_javascript()}
    </script>
</body>
</html>"""

    def _render_stage(self, stage_name: str, grid: Grid, stage_idx: int) -> str:
        """Render a single pipeline stage."""

        # Count components by type
        counts = grid.count_components()
        light_count = counts.get(ComponentType.LIGHT, 0)
        air_count = counts.get(ComponentType.AIR_SUPPLY, 0)
        smoke_count = counts.get(ComponentType.SMOKE_DETECTOR, 0)
        total_count = len(grid.cells)

        # Generate grid cells
        grid_cells = self._render_grid_cells(grid)

        return f"""
        <div class="stage" data-stage="{stage_idx}">
            <div class="stage-header">
                <h2>Stage {stage_idx + 1}: {stage_name}</h2>
                <div class="stage-stats">
                    <div class="stat">
                        <span class="stat-label">Total Components:</span>
                        <span class="stat-value">{total_count}</span>
                    </div>
                    <div class="stat">
                        <span class="stat-label">Lights:</span>
                        <span class="stat-value" style="color: {self.COLORS[ComponentType.LIGHT]}">{light_count}</span>
                    </div>
                    <div class="stat">
                        <span class="stat-label">Air Supply:</span>
                        <span class="stat-value" style="color: {self.COLORS[ComponentType.AIR_SUPPLY]}">{air_count}</span>
                    </div>
                    <div class="stat">
                        <span class="stat-label">Smoke Detectors:</span>
                        <span class="stat-value" style="color: {self.COLORS[ComponentType.SMOKE_DETECTOR]}">{smoke_count}</span>
                    </div>
                </div>
            </div>
            <div class="grid" style="grid-template-columns: repeat({grid.width}, 1fr); grid-template-rows: repeat({grid.height}, 1fr);">
                {grid_cells}
            </div>
        </div>
        """

    def _render_grid_cells(self, grid: Grid) -> str:
        """Render all grid cells with components."""

        # Create a map of (x, y) -> cell for quick lookup
        cell_map = {(cell.x, cell.y): cell for cell in grid.cells}

        # Generate HTML for each cell
        cells_html = []
        for y in range(grid.height):
            for x in range(grid.width):
                cell = cell_map.get((x, y))
                cell_html = self._render_cell(x, y, cell)
                cells_html.append(cell_html)

        return "\n".join(cells_html)

    def _render_cell(self, x: int, y: int, cell) -> str:
        """Render a single grid cell."""

        if not cell or cell.component == ComponentType.EMPTY:
            # Empty cell
            return f"""
                <div class="cell empty" data-x="{x}" data-y="{y}">
                    <span class="cell-coords">{x},{y}</span>
                </div>
            """

        # Cell with component
        color = self.COLORS.get(cell.component, "#CCCCCC")
        type_icon = self._get_component_icon(cell.component)

        return f"""
            <div class="cell filled" data-x="{x}" data-y="{y}" style="background-color: {color};">
                <span class="component-icon">{type_icon}</span>
                <span class="cell-coords">{x},{y}</span>
                <div class="tooltip">
                    <strong>{cell.component.value}</strong><br>
                    Position: ({x}, {y})
                </div>
            </div>
        """

    def _get_component_icon(self, component_type: ComponentType) -> str:
        """Get emoji icon for component type."""
        icons = {
            ComponentType.LIGHT: "üí°",
            ComponentType.AIR_SUPPLY: "üå¨Ô∏è",
            ComponentType.SMOKE_DETECTOR: "üö®",
        }
        return icons.get(component_type, "‚ùì")

